import java.util.Scanner;
class InputMonthNumber2
{
public static void main(String args[])
{
Scanner scob=new Scanner(System.in);
int dm;
dm = scob.nextInt();
if(dm==0){System.out.println("JAN");}
else if(dm==1){System.out.println("FEB");}
else if(dm==1){System.out.println("MAR");}
else if(dm==1){System.out.println("APR");}
else if(dm==1){System.out.println("MAY");}
else if(dm==1){System.out.println("JUNE");}
else if(dm==1){System.out.println("JULY");}
else if(dm==1){System.out.println("AUG");}
else if(dm==1){System.out.println("SEP");}
else if(dm==1){System.out.println("OCT");}
else if(dm==1){System.out.println("NOV");}
else if(dm==1){System.out.println("DEC");}
else{System.out.println("INVALID");}
}
}
